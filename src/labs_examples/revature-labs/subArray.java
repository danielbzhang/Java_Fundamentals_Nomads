public class subArray {
  /**
   * Return only a part of an array. For instance, given the array {1,2,3,4,5}, a
   * sub array with start 1 and end 4
   * would result in {2,3,4}, because the starting index is inclusive, but the
   * ending index is exclusive.
   *
   * You will need to create a new array whose size is only the amound of elements
   * requested between the start and
   * end index, and then write a for loop which copies values from nums over to
   * this new resulting array.
   *
   * @param nums  an array of ints.
   * @param start the starting index, inclusive.
   * @param end   the ending index, exclusive.
   * @return a sub-array of nums containing the values between start and end.
   */
  public int[] sub(int[] nums, int start, int end) {
    int[] newArr = new int[end - start];
    for (int i = start; i < end; i++) {
      newArr[i - start] = nums[i];
    }
    return newArr;
  }
}